#ifndef MB85RCXX_H
#define MB85RCXX_H

/*************************************************************************************************/
/*写一字节数据*/
/*************************************************************************************************/
extern void Mb85rcxx_WriteByte(unsigned short add,unsigned char wbyte);

/*************************************************************************************************/
/*读一字节数据*/
/*************************************************************************************************/
extern unsigned char Mb85rcxx_ReadByte (unsigned short add);

/*************************************************************************************************/
/*将FLOAT型变量存入到铁闪中去
  data:要存入的浮点数 add:要存入的地址，一个浮点数四个字节(一个字节8位)*/
/*************************************************************************************************/
void Mb85rcxx_Write_Float(unsigned short add,float data);

/*************************************************************************************************/
/*将存储的FLOAT型变量从铁闪中读出
  add:要读取的地址*/
/*************************************************************************************************/
extern float Mb85rcxx_Read_Float(unsigned short add);

/*************************************************************************************************/
//写入测量记录
/*************************************************************************************************/
extern void Mb85rcxx_Write_Test_Record(void);

/*************************************************************************************************/
//读取测量记录
//参数:	now_record_number 要读取的测量记录编号，以最新保存的测量记录编号为基础
/*************************************************************************************************/
extern void Mb85rcxx_Read_Test_Record(unsigned char now_record_number);

/*************************************************************************************************/
/*读取试验设置的参数值*/
/*************************************************************************************************/
extern void Mb85rcxx_Read_Test_Set(void);

/*************************************************************************************************/
/*写入试验设置的参数值*/
/*************************************************************************************************/
extern void Mb85rcxx_Write_Test_Set(void);

/*************************************************************************************************/
/*写入试验设置的参数默认值*/
/*************************************************************************************************/
extern void Mb85rcxx_Write_Test_Set_Default(void);

/*************************************************************************************************/
/*读取模拟量校正系数*/
/*************************************************************************************************/
extern void Mb85rcxx_Read_Correction_Coefficient(void);

/*************************************************************************************************/
/*写入模拟量校正系数默认值*/
/*************************************************************************************************/
extern void Mb85rcxx_Write_Correction_Coefficient_Default(unsigned short control_bit);

/*************************************************************************************************/
/*开机读取设置的参数值*/
/*************************************************************************************************/
extern void Mb85rcxx_ReadSheZhi(void);
 
#endif
